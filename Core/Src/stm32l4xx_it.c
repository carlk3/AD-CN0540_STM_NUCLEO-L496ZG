/**
  * @brief This function handles LPUART1 global interrupt.
  */
void LPUART1_IRQHandler(void)
{
  /* USER CODE BEGIN LPUART1_IRQn 0 */

	bool proceed = true;
	if (LL_LPUART_IsActiveFlag_FE(LPUART1)) {
//		framing error
		LL_LPUART_ClearFlag_FE(LPUART1);
		proceed = false;
	}
	if (LL_LPUART_IsActiveFlag_PE(LPUART1)) {
		LL_LPUART_ClearFlag_PE(LPUART1);
//		parity error
		proceed = false;
	}
	if (LL_LPUART_IsActiveFlag_NE(LPUART1)) {
//		noise error
		LL_LPUART_ClearFlag_NE(LPUART1);
		proceed = false;
	}
	if (LL_LPUART_IsActiveFlag_ORE(LPUART1)) {
//		overrun error
		LL_LPUART_ClearFlag_ORE(LPUART1);
	}

	/* Check RXNE flag value in ISR register */
	if (LL_LPUART_IsActiveFlag_RXNE(LPUART1)
			&& LL_LPUART_IsEnabledIT_RXNE(LPUART1)) {
		if (proceed) {
			/* RXNE flag will be cleared by reading of RDR register (done in call) */
			/* Call function in charge of handling Character reception */
			LPUART_CharReception_Callback();
		} else {
			// Clear RXNE flag:
			LL_LPUART_ReceiveData8(LPUART1);
		}
	}

  /* USER CODE END LPUART1_IRQn 0 */
  /* USER CODE BEGIN LPUART1_IRQn 1 */

  /* USER CODE END LPUART1_IRQn 1 */
}

